import tensorflow as tf
from tensorflow import keras
from tensorflow.keras import layers, models
import numpy as np
import matplotlib.pyplot as plt

class_names = [str(i) for i in range(100)]
cifar100 = keras.datasets.cifar100

(x_train, y_train), (x_test, y_test) = cifar100.load_data(label_mode='fine')

x_train, x_test = x_train / 255.0, x_test / 255.0

model = keras.Sequential([
    keras.layers.Flatten(input_shape=(32, 32, 3)),
    keras.layers.Dense(128, activation='relu'),
    keras.layers.Dense(100, activation='softmax')
])

model.compile(
    optimizer='adam',
    loss='sparse_categorical_crossentropy',
    metrics=['accuracy']
)

history = model.fit(
    x_train,
    y_train,
    validation_split=0.1,
    epochs=10
)

plt.imshow(x_train[2000])
plt.axis('off')
plt.show()

plt.imshow(x_test[2000])
plt.axis('off')
plt.show()

plt.figure(figsize=(10,10))
for i in range(20):
    plt.subplot(4, 5, i+1)
    plt.imshow(x_train[i])
    plt.xticks([])
    plt.yticks([])
plt.tight_layout()
plt.show()

model = models.Sequential()
model.add(layers.Conv2D(32, (3,3), activation='relu', input_shape=(32,32,3)))
model.add(layers.MaxPooling2D((2,2)))
model.add(layers.Conv2D(64, (3,3), activation='relu'))
model.add(layers.MaxPooling2D((2,2)))
model.add(layers.Conv2D(64, (3,3), activation='relu'))

model.add(layers.Flatten())
model.add(layers.Dense(128, activation='relu'))
model.add(layers.Dense(100, activation='softmax'))

model.compile(
    optimizer='adam',
    loss='sparse_categorical_crossentropy',
    metrics=['accuracy']
)

history = model.fit(
    x_train,
    y_train,
    validation_data=(x_test, y_test),
    epochs=10
)

plt.plot(history.history['accuracy'])
plt.plot(history.history['val_accuracy'])
plt.legend(['accuracy','val_accuracy'])
plt.show()

plt.plot(history.history['loss'])
plt.plot(history.history['val_loss'])
plt.show()

test_loss, test_accuracy = model.evaluate(x_test, y_test)

test_image = x_test[567]
actual_label = y_test[567][0]

predictions = model.predict(x_test)
pred_probs = predictions[567]
predicted_label = np.argmax(pred_probs)
confidence = pred_probs[predicted_label]

plt.figure(figsize=(6,6))
plt.imshow(test_image)
plt.axis('off')
plt.text(0, -5, f"Actual Class: {actual_label}")
plt.text(0, -15, f"Predicted Class: {predicted_label}")
plt.text(0, -25, f"Confidence: {confidence:.2f}")
plt.show()

